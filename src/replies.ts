function normalizeText(text: string): string {
  return text
    .toLowerCase()
    .trim()
    .replace(/[؟!,.()]/g, "")
    .replace(/\s+/g, " ");
}

function isPositiveAnswer(text: string): boolean {
  return /خوب|عالی|مرسی|ممنون|خداروشکر|بد نیست|شکر|آره|بله|اوکی|حله|باشه|حتما|جوون|آفرین/.test(
    text,
  );
}

function getRandomReply(key: string): string | null {
  const arr = replies[key];
  if (!arr) return null;
  return arr[Math.floor(Math.random() * arr.length)]!;
}

function getBestMatch(text: string): string | null {
  let bestKey: string | null = null;
  let maxLen = 0;
  for (const key of Object.keys(replies)) {
    if (text.includes(key) && key.length > maxLen) {
      maxLen = key.length;
      bestKey = key;
    }
  }
  return bestKey;
}

export function getConversationalReply(
  userId: string,
  text: string,
  messageCount: number,
  isNotified: boolean,
): { reply: string | null; action: "none" | "offer_help" | "promo" } {
  const clean = normalizeText(text);
  const now = Date.now();
  let state = userConversationState.get(userId) || {
    stage: "greeting",
    lastInteraction: 0,
  };

  if (now - state.lastInteraction > 60000) state.stage = "greeting";
  state.lastInteraction = now;

  if (state.stage === "greeting" && messageCount <= 2) {
    userConversationState.set(userId, { ...state, stage: "rapport" });
    const match = getBestMatch(clean);
    if (match) return { reply: getRandomReply(match), action: "none" };
    return { reply: "سلام خوبی؟ چه خبر؟ 😊", action: "none" };
  }

  if (
    state.stage === "rapport" &&
    messageCount >= 3 &&
    messageCount <= 5 &&
    !isNotified
  ) {
    if (
      clean.includes("فیلتر") ||
      clean.includes("نت") ||
      clean.includes("vpn") ||
      clean.includes("کانفیگ")
    ) {
      userConversationState.set(userId, { ...state, stage: "offer_help" });
      return {
        reply:
          "آها اذیت میشی از نت؟ من یه کانال دارم کانفیگ رایگان V2Ray میذاره. میخوای بفرستم؟",
        action: "offer_help",
      };
    }
    if (
      clean.includes("غم") ||
      clean.includes("ناراحت") ||
      clean.includes("دلم گرفته")
    ) {
      userConversationState.set(userId, { ...state, stage: "offer_help" });
      return {
        reply:
          "💙 میدونم بعضی وقتا سخته. یه کانال دارم که فایل صوتی آرامش‌بخش داره. بفرستم واست؟",
        action: "offer_help",
      };
    }
    const match = getBestMatch(clean);
    if (match) return { reply: getRandomReply(match), action: "none" };
    return { reply: "آره؟ بگو بیشتر 😊", action: "none" };
  }

  if (state.stage === "offer_help" && !isNotified) {
    if (
      isPositiveAnswer(clean) ||
      clean.includes("بله") ||
      clean.includes("آره") ||
      clean.includes("بفرست")
    ) {
      userConversationState.set(userId, { ...state, stage: "sent_promo" });
      return {
        reply:
          "✨ عالیه! اینم لینک کانال:\n@configCollectore_Bot\nبعد از عضویت ربات رو استارت کن تا کانفیگ‌ها رو بگیری.",
        action: "promo",
      };
    } else {
      userConversationState.set(userId, { ...state, stage: "rapport" });
      return { reply: "باشه هر وقت خواستی بهم بگو 😊", action: "none" };
    }
  }

  if (isNotified) {
    const match = getBestMatch(clean);
    if (match) return { reply: getRandomReply(match), action: "none" };
    return {
      reply: "😊 خوشحالم که اینجایی. امیدوارم کانفیگ‌ها به دردمون بخوره 😉",
      action: "none",
    };
  }

  const match = getBestMatch(clean);
  if (match) return { reply: getRandomReply(match), action: "none" };
  return { reply: null, action: "none" };
}
