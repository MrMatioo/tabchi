// --- توابع کمکی املایی و معنایی ---
export function isPositiveAnswer(text: string): boolean {
  return /خوب|عالی|مرسی|ممنون|خداروشکر|بد نیست|شکر/.test(text);
}

export function isNegativeAnswer(text: string): boolean {
  return /نه|بد|خراب|داغون|ناراحت|هعی|اه|ناخوش/.test(text);
}

export function isGreeting(text: string): boolean {
  return /سلام|سلاام|درود|س\.آ|سلام علیک|صلام/.test(text);
}

export function isHowAreYou(text: string): boolean {
  return /خوبی|چطوری|حالت.*چطوره|احوال/.test(text);
}

export function isWhatsUp(text: string): boolean {
  return /چخبر|چه.*خبر/.test(text);
}

export function getConversationalReply(userId: string, text: string): string {
  let cleanText = text
    .toLowerCase()
    .trim()
    .replace(/[\u200B-\u200D\uFEFF]/g, "")
    .replace(/[ـ\.،_؟\?!\(\)\*#&]/g, " ")
    .replace(/\s+/g, " ");

  const now = Date.now();

  let state = userConversationState.get(userId) || {
    stage: "idle",
    lastInteraction: 0,
  };

  if (now - state.lastInteraction > 30000) {
    state.stage = "idle";
  }

  if (/\b(اصل|معرفی|خودتو)\b/.test(cleanText)) {
    userConversationState.set(userId, {
      stage: "answered",
      lastInteraction: now,
    });
    return replies.اصل![Math.floor(Math.random() * replies.اصل!.length)]!;
  }

  if (/\b(ربات|بات|باتون|رباتی)\b/.test(cleanText)) {
    userConversationState.set(userId, {
      stage: "answered",
      lastInteraction: now,
    });
    return replies.ربات![Math.floor(Math.random() * replies.ربات!.length)]!;
  }

  if (
    /\b(چند.*سال|سنت|سنی|چند.*سالته)\b/.test(cleanText) ||
    cleanText === "سن"
  ) {
    userConversationState.set(userId, {
      stage: "asked_personal",
      lastInteraction: now,
    });
    return "من ۲۲ سالمه عزیزم! شما چند سالتونه؟";
  }

  if (/\b(درس|دانشگاه|تحصیلات|رشته|چی.*میخونی)\b/.test(cleanText)) {
    userConversationState.set(userId, {
      stage: "asked_personal",
      lastInteraction: now,
    });
    return "من دانشجوی کارشناسی ارشد روانشناسی‌ام، شما چی می‌خونین؟";
  }

  if (/\b(کجایی|اهل|ساکن|شهر|کجا.*زندگی)\b/.test(cleanText)) {
    userConversationState.set(userId, {
      stage: "asked_personal",
      lastInteraction: now,
    });
    return "من ساکن تهرانم، شما اهل کدوم شهر قشنگین؟";
  }

  if (state.stage === "asked_how_are_you") {
    if (isPositiveAnswer(cleanText)) {
      userConversationState.set(userId, {
        stage: "asked_whats_up",
        lastInteraction: now,
      });
      return "خداروشکر قشنگم، خب چه خبرها؟ چیکارا می‌کنی؟";
    } else if (isNegativeAnswer(cleanText)) {
      userConversationState.set(userId, {
        stage: "idle",
        lastInteraction: now,
      });
      return "ای وای، غصه نخور عزیزم! دنیا بالا و پایین داره، بیا یه کم چت کنیم روحیه‌ات عوض شه بابا غصه چیه غمت نباشه 😉😂";
    }
  }

  if (state.stage === "asked_whats_up") {
    userConversationState.set(userId, { stage: "idle", lastInteraction: now });
    if (/خبر|سلامتی|هیچی|سلامت/.test(cleanText)) {
      return "خداروشکر، بی‌خبری خودش بهترین خبره والا تو این زمونه 😂";
    }
    return "آها، که اینطور! انشالله همیشه خیر و خوشی و خنده باشه برات رفیق پایه 😉";
  }

  if (state.stage === "asked_personal") {
    userConversationState.set(userId, { stage: "idle", lastInteraction: now });
    return "آها خوشبختم عزیزم، مرسی که گفتی و باهات آشنا شدم! 👌✨😂";
  }

  if (isHowAreYou(cleanText)) {
    userConversationState.set(userId, {
      stage: "asked_whats_up",
      lastInteraction: now,
    });
    return "منم خیلی خوبم مرسی از احوالپرسی‌تون، شما چه خبرها؟ روزتون چطور بود؟";
  }

  if (isWhatsUp(cleanText)) {
    userConversationState.set(userId, { stage: "idle", lastInteraction: now });
    return "سلامتی، خبری نیست والا، مشغول کارهای روزمره... شما چه خبر؟";
  }

  if (isGreeting(cleanText)) {
    userConversationState.set(userId, {
      stage: "asked_how_are_you",
      lastInteraction: now,
    });
    return "سلام عزیزم خوبی؟ روزت بخیر";
  }

  let bestMatchKey: string | null = null;
  let maxMatchScore = 0;

  for (const key of Object.keys(replies)) {
    const patternStr = key.replace(/_/g, ".*");
    const regex = new RegExp(patternStr, "i");

    if (regex.test(cleanText)) {
      if (key.length > maxMatchScore) {
        maxMatchScore = key.length;
        bestMatchKey = key;
      }
    }
  }

  if (bestMatchKey && replies[bestMatchKey]) {
    userConversationState.set(userId, {
      stage: "answered",
      lastInteraction: now,
    });
    const list = replies[bestMatchKey]!;
    return list[Math.floor(Math.random() * list.length)]!;
  }

  userConversationState.set(userId, { stage: "idle", lastInteraction: now });
  const defaults = [
    "عه واقعاً؟ 🤔 چقدر جالب، بیشتر بگو برام 😂",
    "هوممم درسته عزیزم، جالب شد... ادامه بده شنونده‌ام",
    "آها! 👌 خوشحالم که داری تعریف می‌کنی، گوشم با توئه سراپا گوشم.",
    "خوببب پس اینطور، دیگه چه خبر؟ تعریف کن بنال ببینم چی تو چنته داری 😂",
    "عجببب 🚶‍♀️ روزگاری شده ها، تهش خندست ولش کن دنیا رو سخت نگیر.",
    "هومم، شنیده بودم راجع بهش ولی از زبان تو جذاب‌تره.",
  ];
  return defaults[Math.floor(Math.random() * defaults.length)]!;
}
