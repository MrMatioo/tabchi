export function isPositiveAnswer(text: string): boolean {
  return /خوب|عالی|مرسی|ممنون|خداروشکر|بد نیست|شکر|آره|بله|اوکی|حله|باشه|حتما|جوون|آفرین|دستت درد نکنه/.test(
    text,
  );
}

export function isNegativeAnswer(text: string): boolean {
  return /نه|بد|خراب|داغون|ناراحت|هعی|اه|ناخوش|خز|ضایع|پرت/.test(text);
}

export function isGreeting(text: string): boolean {
  return /سلام|سلاام|درود|س\.آ|سلام علیک|صلام|hi|hey|هی/.test(text);
}

export function isHowAreYou(text: string): boolean {
  return /خوبی|چطوری|حالت.*چطوره|احوال|چی میگی/.test(text);
}

export function isWhatsUp(text: string): boolean {
  return /چخبر|چه.*خبر|چیکار میکنی|چیکاری|چه خبرا|جوونی/.test(text);
}

export function getConversationalReply(
  userId: string,
  text: string,
): string | null {
  const cleanText = text
    .toLowerCase()
    .trim()
    .replace(/[\u200B-\u200D\uFEFF]/g, "")
    .replace(/[ـ\.،_؟\?!\(\)\*#&]/g, " ")
    .replace(/\s+/g, " ");

  const now = Date.now();
  const state = userConversationState.get(userId) || {
    stage: "idle",
    lastInteraction: 0,
  };

  if (now - state.lastInteraction > 30000) state.stage = "idle";

  if (state.stage === "asked_how_are_you") {
    if (isPositiveAnswer(cleanText)) {
      userConversationState.set(userId, {
        stage: "asked_whats_up",
        lastInteraction: now,
      });
      return "خداروشکر قشنگم، خب چه خبرها؟ چیکارا می‌کنی؟";
    } else if (isNegativeAnswer(cleanText)) {
      userConversationState.set(userId, {
        stage: "idle",
        lastInteraction: now,
      });
      return "ای وای، غصه نخور عزیزم! بیا یه کم چت کنیم روحیه‌ات عوض شه 😉😂";
    }
  }

  if (state.stage === "asked_whats_up") {
    userConversationState.set(userId, { stage: "idle", lastInteraction: now });
    if (/خبر|سلامتی|هیچی|سلامت|هیچ|چیز خاص/.test(cleanText)) {
      return "خداروشکر، بی‌خبری خودش بهترین خبره والا تو این زمونه 😂";
    }
    return "آها، که اینطور! انشالله همیشه خیر و خوشی باشه برات 😉";
  }

  if (
    /\b(چند.*سال|سنت|سنی|چند.*سالته)\b/.test(cleanText) ||
    cleanText === "سن"
  ) {
    userConversationState.set(userId, {
      stage: "asked_personal",
      lastInteraction: now,
    });
    return "من ۲۲ سالمه عزیزم! شما چند سالتونه؟";
  }
  if (/\b(درس|دانشگاه|تحصیلات|رشته|چی.*میخونی)\b/.test(cleanText)) {
    userConversationState.set(userId, {
      stage: "asked_personal",
      lastInteraction: now,
    });
    return "من دانشجوی کارشناسی ارشد روانشناسی‌ام، شما چی می‌خونین؟";
  }
  if (/\b(کجایی|اهل|ساکن|شهر|کجا.*زندگی)\b/.test(cleanText)) {
    userConversationState.set(userId, {
      stage: "asked_personal",
      lastInteraction: now,
    });
    return "من ساکن تهرانم، شما اهل کدوم شهر قشنگین؟";
  }

  if (state.stage === "asked_personal") {
    userConversationState.set(userId, { stage: "idle", lastInteraction: now });
    return "آها خوشبختم عزیزم، مرسی که گفتی! 👌✨😂";
  }

  let bestMatchKey: string | null = null;
  let maxMatchScore = 0;

  for (const [key, regex] of compiledRegexMap.entries()) {
    if (regex.test(cleanText) && key.length > maxMatchScore) {
      maxMatchScore = key.length;
      bestMatchKey = key;
    }
  }

  if (bestMatchKey && replies[bestMatchKey]) {
    userConversationState.set(userId, {
      stage: isGreeting(cleanText) ? "asked_how_are_you" : "answered",
      lastInteraction: now,
    });
    const list = replies[bestMatchKey]!;
    return list[Math.floor(Math.random() * list.length)]!;
  }

  userConversationState.set(userId, { stage: "idle", lastInteraction: now });
  return null;
}
